function previewSortValue(fileName) {
  const match = fileName.match(/^(\d+)[_-]/)
  return match ? Number(match[1]) : Number.MAX_SAFE_INTEGER
}

function previewKey(fileName) {
  return fileName.replace(/\.[^.]+$/, '')
}

function humanizeKey(key) {
  return key
    .replace(/^\d+[_-]/, '')
    .split(/[_-]+/)
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ')
}

function escapeHtml(value) {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
}

export function listDesktopPreviewItems(baseDir = desktopPreviewDir) {
  if (!existsSync(baseDir)) {
    throw new Error(`Missing desktop preview image directory: ${baseDir}`)
  }

  return readdirSync(baseDir)
    .filter((fileName) => /\.(png|jpe?g|webp)$/i.test(fileName))
    .sort((a, b) => {
      const numberDiff = previewSortValue(a) - previewSortValue(b)
      return numberDiff || a.localeCompare(b)
    })
    .map((fileName) => ({
      fileName,
      key: previewKey(fileName),
    }))
}

function labelFor(item, lang) {
  return labels[lang]?.[item.key] ?? humanizeKey(item.key)
}

export function renderDesktopPreviewTable(items, lang, options = {}) {
  const columns = options.columns ?? 4
  const width = `${Math.floor(100 / columns)}%`
  const rows = []

  for (let index = 0; index < items.length; index += columns) {
    rows.push(items.slice(index, index + columns))
  }

  const lines = ['<table>']

  for (const row of rows) {
    lines.push('  <tr>')
    for (const item of row) {
      const label = escapeHtml(labelFor(item, lang))
      const imagePath = `docs/images/desktop_ui/${item.fileName}`
      lines.push(`    <td align="center" width="${width}"><img src="${imagePath}" alt="${label}"><br><b>${label}</b></td>`)
    }
    lines.push('  </tr>')
  }

  lines.push('</table>')
  return lines.join('\n')
}

export function renderDesktopPreviewBlock(items, lang) {
  return [
    DESKTOP_PREVIEW_START,
    '<!-- Generated by `npm run docs:update-desktop-preview`. Edit screenshots in `docs/images/desktop_ui/` or labels in `scripts/docs/update-desktop-preview.mjs`. -->',
    renderDesktopPreviewTable(items, lang),
    DESKTOP_PREVIEW_END,
  ].join('\n')
}

export function replaceDesktopPreviewBlock(content, nextBlock, heading) {
  const startIndex = content.indexOf(DESKTOP_PREVIEW_START)
  const endIndex = content.indexOf(DESKTOP_PREVIEW_END)

  if (startIndex !== -1 || endIndex !== -1) {
    if (startIndex === -1 || endIndex === -1 || endIndex < startIndex) {
      throw new Error('Malformed desktop preview markers')
    }

    return [
      content.slice(0, startIndex).trimEnd(),
      nextBlock,
      content.slice(endIndex + DESKTOP_PREVIEW_END.length).trimStart(),
    ].join('\n\n')
  }

  const headingIndex = content.indexOf(heading)
  if (headingIndex === -1) {
    throw new Error(`Missing heading: ${heading}`)
  }

  const tableStart = content.indexOf('<table>', headingIndex)
  const tableEnd = content.indexOf('</table>', tableStart)
  if (tableStart === -1 || tableEnd === -1) {
    throw new Error(`Missing desktop preview table after heading: ${heading}`)
  }

  return [
    content.slice(0, tableStart).trimEnd(),
    nextBlock,
    content.slice(tableEnd + '</table>'.length).trimStart(),
  ].join('\n\n')
}

export function updateDesktopPreviewReadmes(options = {}) {
  const check = options.check ?? false
  const items = listDesktopPreviewItems(options.imageDir ?? desktopPreviewDir)
  const changed = []

  for (const target of options.targets ?? readmeTargets) {
    const content = readFileSync(target.file, 'utf8')
    const nextBlock = renderDesktopPreviewBlock(items, target.lang)
    const nextContent = replaceDesktopPreviewBlock(content, nextBlock, target.heading)

    if (content === nextContent) {
      continue
    }

    changed.push(target.file)
    if (!check) {
      writeFileSync(target.file, nextContent)
    }
  }

  return changed
}

function main() {
  const check = process.argv.includes('--check')
  const changed = updateDesktopPreviewReadmes({ check })

  if (changed.length === 0) {
    console.log('Desktop preview tables are up to date.')
    return
  }

  if (check) {
    console.error('Desktop preview tables are stale:')
    for (const file of changed) {
      console.error(`  - ${path.relative(repoRoot, file)}`)
    }
    console.error('Run: npm run docs:update-desktop-preview')
    process.exit(1)
  }

  for (const file of changed) {
    console.log(`Updated ${path.relative(repoRoot, file)}`)
  }
}

if (fileURLToPath(import.meta.url) === path.resolve(process.argv[1] ?? '')) {
  main()
}
